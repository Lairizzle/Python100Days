#Remeber that order of operations is PEMDAS
#Parenthesis, Exponenents, Multiplication, Division, Addition, Subtraction
#()
#**
#*/
#+-

#Addition +
print(3+5)

#Subtraction -
print(7-4)

#Multiplication *
print(3*2)

#Division (Division will produce float) /
print(6/3)

#Exponent **
print(2**3)

#Example of PEMDAS - note that Multiplication and Division share priority
#It will read left to right, so in this instance (9+1-3) = 7
print(3*3+3/3-3)

