#Strings
print("Strings")
print("##############################")
print("Putting quotes around any characters will make it a string.")
print("Hello")

#Print a string and reference and index in the string
print("If you use [0] after the quotes of a string you can call an index of the string (substring).")
print("Hello"[0])

print("Placing integers inside of quotes will attribute the string data type and operators will concatenate them instead of adding them. (123+456)")
#The code below will concatenate the strings not add them
print("123"+"456\n")

#Integers (whole numbers without decimals)
print("Integers")
print("##############################")
print(123)
print("Now instead of concatenating the integers, operators like + will add them (123+456)")
print(123+456)
print("You can make large numbers easier to read by using underscores, it will be interpreted as a whole number. (123_456_789")
print(123_456_789)
print("")

#Float (Numbers with decimals, floating point numbers)
print("Floats (Numbers with decimals)")
print("##############################")
print(3.14)
print("")

#Boolean (True or False)
print("Boolean")
print("##############################")
print(True)
print(False)